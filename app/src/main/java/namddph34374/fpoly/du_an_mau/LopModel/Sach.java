package namddph34374.fpoly.du_an_mau.LopModel;

public class Sach {
    private int mas;
    private String tens;
    private int gias;
    private int mals;


    public Sach(String tenSach, String s, String selectedLoaiSach) {
    }

    public Sach( String tens, int gias, int mals) {
        this.mas = mas;
        this.tens = tens;
        this.gias = gias;
        this.mals = mals;

    }

    public int getMas() {
        return mas;
    }

    public void setMas(int mas) {
        this.mas = mas;
    }

    public String getTens() {
        return tens;
    }

    public void setTens(String tens) {
        this.tens = tens;
    }

    public int getGias() {
        return gias;
    }

    public void setGias(int gias) {
        this.gias = gias;
    }

    public int getMals() {
        return mals;
    }

    public void setMals(int mals) {
        this.mals = mals;
    }


}
